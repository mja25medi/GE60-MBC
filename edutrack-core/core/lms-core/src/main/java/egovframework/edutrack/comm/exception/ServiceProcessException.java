package egovframework.edutrack.comm.exception;


public class ServiceProcessException
		extends MediopiaDefineException {

	private static final long	serialVersionUID	= 7924297674439806115L;

	public ServiceProcessException() {
		super();
	}

	public ServiceProcessException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServiceProcessException(String message) {
		super(message);
	}

	public ServiceProcessException(Throwable cause) {
		super(cause);
	}
	
	

}
