package egovframework.edutrack.comm.exception;


public class AjaxIllegalFormatException
		extends AjaxProcessException {

	private static final long	serialVersionUID	= -7924760429764770661L;

	public AjaxIllegalFormatException() {
		super();
	}

	public AjaxIllegalFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public AjaxIllegalFormatException(String message) {
		super(message);
	}

	public AjaxIllegalFormatException(Throwable cause) {
		super(cause);
	}
	
}
